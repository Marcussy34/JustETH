import { useState } from 'react';
import { useRouter } from 'next/router';
import { IDKitWidget } from '@worldcoin/idkit';
import { ToastContainer, toast } from 'react-toastify';
import 'react-toastify/dist/ReactToastify.css';
import Navbar from '../components/Navbar'; // Import the Navbar component
import Footer from '../components/Footer'; // Import the Footer component

export default function OrderConfirmation() {
  const router = useRouter();
  const [review, setReview] = useState('');
  const [rating, setRating] = useState(0);
  const [isWorldIDVerified, setIsWorldIDVerified] = useState(false);

  const handleReviewSubmit = async () => {
    if (!isWorldIDVerified) {
      toast.error('Please verify with WorldID before submitting your review.');
      return;
    }

    console.log('Submitting review:', { review, rating });
    toast.success('Review submitted successfully!');
    setTimeout(() => router.push('/'), 2000);
  };

  const handleWorldIDVerification = () => {
    setIsWorldIDVerified(true);
    toast.success('WorldID verification successful!');
  };

  return (
    <div className="min-h-screen flex flex-col bg-gradient-to-br from-purple-400 via-pink-500 to-red-500">
      <Navbar /> {/* Add the Navbar component */}
      
      <main className="flex-grow flex items-center justify-center pt-24 pb-12"> {/* Added padding for spacing */}
        <div className="bg-white p-8 rounded-lg shadow-lg max-w-md w-full">
          <h1 className="text-2xl font-bold mb-4 text-gray-800">Thank you for your order!</h1>
          <p className="mb-4 text-gray-700">We hope you enjoy your meal. Would you like to leave a review?</p>
          
          <div className="mb-4">
            <label htmlFor="review" className="block text-gray-700 mb-2">Your Review:</label>
            <textarea
              id="review"
              className="w-full p-2 border rounded text-black"
              rows="4"
              value={review}
              onChange={(e) => setReview(e.target.value)}
            ></textarea>
          </div>
          
          <div className="mb-4">
            <label className="block text-gray-700 mb-2">Rating:</label>
            <div className="flex">
              {[1, 2, 3, 4, 5].map((star) => (
                <button
                  key={star}
                  onClick={() => setRating(star)}
                  className={`text-2xl ${star <= rating ? 'text-yellow-400' : 'text-gray-300'}`}
                >
                  â˜…
                </button>
              ))}
            </div>
          </div>

          <div className="mb-4">
            <IDKitWidget
              action="review-submission"
              signal="user-review"
              onSuccess={handleWorldIDVerification}
              app_id="YOUR_WORLD_ID_APP_ID"
            >
              {({ open }) => (
                <button
                  onClick={open}
                  className="w-full bg-green-500 text-white font-semibold py-2 px-4 rounded-md hover:bg-green-600 transition duration-300"
                >
                  Verify with WorldID
                </button>
              )}
            </IDKitWidget>
            <p className="mt-2 text-sm text-gray-600">
              We use WorldID to ensure reviews are authentic and not generated by bots.{' '}
              <span className="text-blue-500 underline cursor-pointer">
                Read more
              </span>
            </p>
          </div>

          <button
            onClick={handleReviewSubmit}
            className="w-full bg-blue-600 text-white font-semibold py-2 px-4 rounded-md hover:bg-blue-700 transition duration-300"
          >
            Submit Review
          </button>
        </div>
      </main>
      
      <ToastContainer position="top-center" autoClose={2000} />
      <Footer /> {/* Add the Footer component */}
    </div>
  );
}
